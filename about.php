				<h2>Vision</h2>
				<h3>Authentic</h3>
				<p>We highly value and strive to be real people following Christ in a real and lost world. We never want to create the illusion that we are perfect people who breeze through life as if we own no hardship or struggle. This allows us an entry to truly build relationships with believers and non-believers alike. It builds a universal bond as we journey through life.</p>
				<h3>Community</h3>
				<p>We long to be a Biblical community. Like the early church we value spending time together, praying together, learning together, and experiencing God together. We deeply desire to care for one another. Community is a basic human core value. We want to take that value and show how true community in Christ is deeper and better than any other kind.</p>
				<h3>Shine</h3>
				<p>We want as a community and as individuals, to live out Matthew 5:16, "Let your light shine before men in such a way that they see your good works, and glorify your Father who is in heaven." It is essential that the world see our faith. It is not enough for them to simply hear. Jesus consistently brought the truth, and with it proof of that truth in His behavior. We must do the same. We value shining in our neighborhoods, work places, families, and with our friends. We believe that the early believers lived or "shined" in such a way that their lives served as one of the key mechanisms that God used to grow His church (Acts 2).</p>
				<h3>God's Glory</h3>
				<p>We strive to do all we do for the glory of our God. God's glory is our rallying point. It is why we come every week and diligently do all that we do. This includes all the acts of service whether big or small: worship to clean up, set up to teaching it up. All acts should be done for God's glory, to make much of Him, not of ourselves. In this, we are most joyful and fulfilled. Outside of that, we are tired and defeated. We believe if we stay focused on Christ and showing Him to magnificent, God will bless our acts of worship and grant us success in accomplishing His will.</p>
